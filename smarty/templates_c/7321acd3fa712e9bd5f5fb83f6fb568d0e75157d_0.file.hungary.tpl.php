<h2 class="region_subheader">Location &amp; History</h2>

<p>A gateway between East and West, Hungary's wine culture as well as its cuisine and language, is the remarkable confluence of foreign elements into something unique.
</p>
<p>
Hungary, like its capital Budapest &mdash; once two cities, Buda on the right bank and Pest on the left bank &mdash; are bisected by the Danube River. At the center of an alluvial basin of what millions of years ago was the Pannonian sea, the country is mostly flat with a continental climate, an agricultural breadbasket people has been drawn to and often clashed in the past over its fertile conditions. 
</p>
<div class="alignCenter  winery_caption some_space2"><img src="/img/wineries/gere_vineyard.jpg" alt="Vineyards in Villany" class="winery_postcardcenter" /><br>Gere Vineyard in Villany</div>
<p>
Organized viticulture in the lands that became Hungary dates back more than two thousand years. The Hungarian tribes responsible for developing the local wine culture were deeply influenced by traditions brought from Central Asia, Roman practices, and advanced methods from Western Europe. Ancient cultivars from the East that adapted best to the region’s diverse, often volcanic soils are partly why Hungarian wines are so distinctive. 
</p>
<p>
By the 16th century, Hungarian wines were the toast of Europe and the favorites of royal courts. Along the way, a vital national winegrowing tradition took root, strong enough to withstand the devastation of phylloxera but the Communist period that followed World War II saw the collectivization of production and the greatness of the many regional wines was largely forgotten. Today, only 20 years after the re-establishment of private and family wineries, Hungary is in the midst of a wine renaissance. The potential of its 22 distinct appellations and breadth of indigenous varieties and traditions of winemaking are only now being truly (re)discovered.
</p>



<h2 class="region_subheader">Regions &amp; Grapes</h2>
<p>
The diversity of historical influences is matched by the large number of winegrowing areas, varying in soil, climate and regional specialties. The Great Plain stretching south from Budapest down the middle of the country accounts for half the wine production, with vineyards providing erosion control in the vast expanses of sandy soil. Wines of quality, even of legend, come from the pockets of hillside vineyards that dot the arc of mountainous terrain stretching across the country—Villány, Szekszárd and the Lake Balaton region to the west, Somló to the northwest, Eger (home of Egri Bikaver, the hearty Bull’s Blood) and Tokaj in the northeast.
</p>
<div class="alignCenter  winery_caption some_space2"><img  src="/img/wineries/bott_buried_cellars.jpg" alt="Underground cellars" class="winery_postcardcenter" /><br>Underground cellars in Tokaj</div>

<h2 class="region_subheader">Tokaj</h2>
<p>
Eden Abandoned &mdash; No region has made the impression on us at Blue Danube Wine Co. as the volcanic hills and winding rivers of Tokaj-Hegyalja. It is to us what Burgundy is to others. A chain of 400 volcanoes of impossible geological and microclimatological complexity, a long history as a wine region and a plethora of indigenous varieties and styles of wine. After the fall of Communism, some say there is a wine renaissance underway in Tokaj today. We are inclined to agree.

</p>

<h2 class="region_subheader">Somló</h2>
<p>Little appellation, big WINES &mdash; Millions of years ago, Somló, Hungary’s smallest appellation, was an underwater volcano. Its southerly slopes of ancient sea sediment and basalt are home to some of Hungary’s steepest, most densely planted vineyards. Minutely divided, these rarely trellised parcels of vines are workable only by hand. Somló’s exclusively white wines are typically made from Hárslevelű, Furmint, Olaszrizling, or the local rarity Juhfark (Sheep’s Tail). Volcanic soil permeates every sip and the two producers below are the most distinctive we’ve ever tasted.

</p>

<h2 class="region_subheader">Current Trends</h2>
<p>
In step with broader European winemaking trends, red grapes have been on the rise in recent decades. Renewed interest in the indigenous <a href="/resources/grape_varietals/kadarka/" title="Kadarka">Hungarian Kadarka</a> and common Central European varieties like <a href="/resources/grape_varietals/blaufrankisch/" title="Blaufränkisch">Blaufränkisch</a> (Kékfrankos) and <a href="/resources/grape_varietals/zweigelt/" title="Zweigelt">Zweigelt</a> has blossomed alongside plantings of Cabernet Sauvignon, Pinot Noir and other international varieties. Matching the range of red grapes to the right regions and improving practices in the vineyard and winery has yielded excellent single-variety bottles that can compete on the international stage.
</p>
<div class="alignCenter  winery_caption some_space2"><img src="/img/regions/hungary_bottles.jpg" alt="Hungarian wine selection" class="winery_postcardcenter" /></div>
<p>
Perhaps the most dramatic improvement has come in the quality of blended wines, from prestigious new-wave cuvées that define a particular house style to updated versions of traditional blends like Egri Bikaver. In the decades of Communist preference for quality over quantity, the once-respected Bull’s Blood sank into disrepute. But with modern production techniques, and the addition of Cabernet Sauvignon, Cabernet Franc, Merlot and Pinot Noir to the range of blending options, premium Bull’s Blood is back.
</p>
<p>
White wines have a long track record in Hungary, offering a perfect foil for the intense, pungent qualities of traditional cuisine. The most important whites are still <a href="/resources/grape_varietals/furmint/" title="Furmint">Furmint</a> and <a href="/resources/grape_varietals/harslevelu/" title="Hárslevelü">Hárslevelü</a> (the grapes of Tokaj), along with  <a href="/resources/grape_varietals/riesling/" title="Riesling">Riesling</a>, <a href="/resources/grape_varietals/welschriesling/" title="Welschriesling">Welschriesling</a> (Olaszrizling in Hungarian, and no relation to German Riesling) and <a href="/resources/grape_varietals/gruner_veltliner/" title="Zöld Veltlini">Zöld Veltlini</a> (Austrian Grüner Veltliner); Chardonnay, Sauvignon Blanc, Pinot Blanc, Pinot Gris, and the rare <a href="/resources/grape_varietals/juhfark/" title="Juhfark">Juhfark</a> are getting increasing attention.
</p>

