<h2 class="region_subheader">Qvevri winemaking: a unique Georgian tradition</h2>
<p>It would be an oversimplification to say that a qvevri is a clay vessel used for centuries to produce wine in Georgia. The traditional practice of qvevri winemaking has been producing natural wines without the use of chemicals, foreign yeasts, or filtration long before it became a stylistic trend. 
</p>
<div class="alignCenter  winery_caption some_space2"><img src="/img/regions/georgia_qvevri.jpg" alt="Qvevris" class="winery_postcardcenter" /></div>
<p>A qvevri is a thick-walled vessel buried deep in the ground in a marani, or Georgian wine cellar. As such, it naturally maintains wine at optimal temperature during fermentation and allows it to age for many years without spoilage. Additionally, its conical shape and small pointed base allow for the seeds, pomace, and other sediment to settle to the bottom of the vessel. The interaction of the wine with its sediment adds more complexity and body to the wine while also keeping it chemically stable. Once fermentation is complete, the wine can be racked into another qvevri, leaving the heavy sediment behind.
</p>
<p>
The liming of the exterior of qvevris is another unique practice. This helps prevent mold buildup on the outside and preserve the wine when higher than normal temperatures occur during fermentation. The inside is also sometimes lined with beeswax when the clay is too porous, preventing the wine from becoming oxidized or contaminated.
</p>
<p>When the wine is ready, a lid either made of clay or wood, is placed over the top of the qvevri. The lid is secured with clay, and often sulphur wicks to create a clean environment and form a vacuum between the wine and the lid when the sulphur burns. A covering of earth is packed around the lid to insure a secure airtight closure.
</p>
<p>
While the wine is aging in the qvevri, it interacts with the vessel in a unique way that enhances its distinctive character. White wines made in qvevri are often noted for their color. The extended maceration on the skins provides more pigment to the juice, resulting in &ldquo;white&rdquo; wines that appear amber, orange, or gold.
</p>
<div class="alignCenter  winery_caption some_space2"><a title="Georgian Amber Wine Winemaking" href="/img/regions/georgia_orangewine.jpg" target="_blank"><img  src="/img/regions/georgia_orangewine.jpg" alt="Georgian Amber Wine Winemaking"  class="winery_75"/></a><br>Drawing by Elaine Chukan Brown, <a href="http://wakawakawinereviews.com" target="_blank">Wakawaka Wine Reviews</a></div>

<p>
Sadly, the art of making qvevris is being lost as modern winemakers  often choose to vinify their wines in barriques for a more Western style. Qvevris are also harder to make, clean and repair than wooden barrels. There are only a few artisans left who understand the difficult art of qvevri winemaking. Thankfully, there is support to continue this unique tradition. In 2013, UNESCO added qvevri winemaking to its Lists of the Intangible Cultural Heritage of Humanity. So as you sip a wine from Georgia, consider how 8,000 years of practice have gone into the production of the special liquid you hold in your hands.
</p>

